export const randomName = (): string => {
    const pre = arrayRandom(prefixes);
    for (;;) {
        var suf = arrayRandom(suffixes);
        if (pre === suf) {
            continue;
        }
        if (pre === 'Avenging' && suf === 'Avenger') {
            continue;
        }
        if (pre === 'Blast' && suf === 'Blaster') {
            continue;
        }
        if (pre === 'Certain' && suf === 'Certainty') {
            continue;
        }
        if (pre === 'Cyber' && suf === 'Cyborg') {
            continue;
        }
        if (pre === 'Dashing' && suf === 'Dasher') {
            continue;
        }
        if (pre === 'Devil' && suf === 'Demon') {
            continue;
        }
        if (pre === 'Dreaming' && suf === 'Dream') {
            continue;
        }
        if (pre === 'Flying' && suf === 'Flier') {
            continue;
        }
        if (pre === 'Industrial' && suf === 'Industry') {
            continue;
        }
        if (pre === 'Lunar' && suf === 'Moon') {
            continue;
        }
        if (pre === 'Lurking' && suf === 'Lurker') {
            continue;
        }
        if (pre === 'Mysterious' && suf === 'Mystery') {
            continue;
        }
        if (pre === 'Original' && suf === 'Origin') {
            continue;
        }
        if (pre === 'Night' && suf === 'Nightmare') {
            continue;
        }
        if (pre === 'Radiant' && suf === 'Radiance') {
            continue;
        }
        if (pre === 'Sleepy' && suf === 'Sleep') {
            continue;
        }
        if (pre === 'Solar' && suf === 'Sun') {
            continue;
        }
        if (pre === 'Thunder' && suf === 'Lightning') {
            continue;
        }
        if (pre === 'Towering' && suf === 'Tower') {
            continue;
        }
        if (pre === 'War' && suf === 'Warrior') {
            continue;
        }
        if (pre === 'Wish' && suf === 'Wisher') {
            continue;
        }
        if (titles.includes(pre) && titles.includes(suf)) {
            continue;
        }
        break;
    }
    return pre + suf;
}
